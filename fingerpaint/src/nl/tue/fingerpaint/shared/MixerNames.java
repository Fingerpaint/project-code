package nl.tue.fingerpaint.shared;

/**
 * <p>
 * A class that contains the mixer names for all the different geometries
 * that are used in the Fingerpaint application.
 * </p>
 * 
 * @author Group Fingerpaint
 */
public class MixerNames {

	/**
	 * <p>
	 * All mixer names that are applicable for the rectangular geometry.
	 * </p>
	 * 
	 * @author Group Fingerpaint
	 * @see GeometryNames#RECT
	 */
	public static class RectMixers {
		
		/** The default mixer for the rectangular geometry. */
		public static final String DEFAULT = "Default";
		
	}
	
	/**
	 * <p>
	 * All mixer names that are applicable for the square geometry.
	 * </p>
	 * 
	 * @author Group Fingerpaint
	 * @see GeometryNames#SQR
	 */
	public static class SqrMixers {
		
		/** The default mixer for the square geometry. */
		public static final String DEFAULT = "Default";
		
	}
	
}
